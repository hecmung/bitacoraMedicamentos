    public void traerDatos() {
        listamedicamentos = null;
        if (true == archivo.existenciaArchivo(jLabel2.getText())) {
            try {
                listamedicamentos = archivo.mostarArchivoDatosUsuarios(jLabel2.getText());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Agregar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void refrescarCombo() {
        jComboBox1.removeAllItems();
        if (listamedicamentos != null) {
            Iterator<Medicamento> itrlistausuarios = listamedicamentos.iterator();
            while (itrlistausuarios.hasNext()) {
                medicamento = itrlistausuarios.next();
                jComboBox1.addItem(medicamento.getNombremed());
            }

        }
    }

